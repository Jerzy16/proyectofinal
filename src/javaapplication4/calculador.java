    private void botonigualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonigualActionPerformed
      double segundonumero;
    if(operaciones.equals("cal")){
        txtpantallaR.setText(cadenanumeros); 
     }
     else if(operaciones.equals("restar")){
         segundonumero = Double.parseDouble(cadenanumeros);
         resultado = primernumero - segundonumero;
         txtpantallaR.setText(String.format("% .2f",resultado));
         cadenanumeros = String.valueOf(resultado);
         operaciones = "cal";
         txtpantallaO.setText("");
         
     }
     else if(operaciones.equals("sumar")){
         segundonumero = Double.parseDouble(cadenanumeros);
         resultado = primernumero + segundonumero;
         txtpantallaR.setText(String.format("% .2f",resultado));
         cadenanumeros = String.valueOf(resultado);
         operaciones = "cal";
         txtpantallaO.setText("");
     }
      else if(operaciones.equals("multiplicar")){
         segundonumero = Double.parseDouble(cadenanumeros);
         resultado = primernumero * segundonumero;
         txtpantallaR.setText(String.format("% .2f",resultado));
         cadenanumeros = String.valueOf(resultado);
         operaciones = "cal";
         txtpantallaO.setText("");
     }
      else if(operaciones.equals("dividir")){
         segundonumero = Double.parseDouble(cadenanumeros);
         if(segundonumero == 0){
           txtpantallaR.setText("NoSeDividePor0");
         }else{resultado = primernumero / segundonumero;
         txtpantallaR.setText(String.format("% .2f",resultado));
         cadenanumeros = String.valueOf(resultado);
         operaciones = "cal";
            txtpantallaO.setText(""); 
         }
     }
    
     activado = true;
    }//GEN-LAST:event_botonigualActionPerformed

